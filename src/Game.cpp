Game::Game() : m_windowSize(640, 400), m_window(m_windowSize, "Raycaster"), m_renderer(m_window.GetWindow()), m_map(grid)
{
    m_player.m_position = {12.0f, 12.0f};
}

Game::~Game()
{
}

void Game::ProcessInput()
{
    m_window.ProcessInput(m_player);
}

void Game::Update(float deltaTime)
{
    m_player.Update(deltaTime, m_map);
    m_renderer.CastRays(m_player, m_map);
}

void Game::Render()
{
    m_renderer.BeginDraw();

    m_renderer.ClearColorBuffer(0xFF000000);
    m_renderer.Generate3DView(m_player);
    m_renderer.RenderColorBuffer();

    m_renderer.EndDraw();
}

void Game::Run()
{
    Uint64 ticksLastFrame = 0;
    while(m_window.IsRunning())
    {
        float deltaTime = (SDL_GetTicks64() - ticksLastFrame) / 1000.0f;
        ticksLastFrame = SDL_GetTicks64();
        ProcessInput();
        Update(deltaTime);
        Render();
        m_window.SetWindowTitle("Raycaster - FPS: " + std::to_string(1.0f / deltaTime));
    }
}
